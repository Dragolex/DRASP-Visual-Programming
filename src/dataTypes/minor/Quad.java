package dataTypes.minor;

public class Quad<U, V, W, X> {

	    public U first;
	    public V second;
	    public W third;
	    public X fourth;

	    public Quad(U first, V second, W third, X fourth)
	    {
	        this.first = first;
	        this.second = second;
	        this.third = third;
	        this.fourth = fourth;
	    }

		public Quad() {}
}
